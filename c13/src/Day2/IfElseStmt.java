package Day2;

public class IfElseStmt {

	public static void main(String[] args) {
		//basic if else statement
		int i = 1;
		// if the condition is not satisfied the else statement will be executed
		if (i > 5) {
			System.out.println("True");
		}
		else {
			System.out.println("False");
		}
	}

}